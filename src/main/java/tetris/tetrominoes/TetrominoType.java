package tetris.tetrominoes;

/**
 * Enumeration representing the seven standard Tetromino shapes.
 * <p>
 * Names correspond to the letter the shape resembles:
 * <ul>
 * <li><b>I:</b> The straight line (Cyan)</li>
 * <li><b>O:</b> The square (Yellow)</li>
 * <li><b>T:</b> The T-shape (Purple)</li>
 * <li><b>J:</b> The L-shape with left hook (Blue)</li>
 * <li><b>L:</b> The L-shape with right hook (Orange)</li>
 * <li><b>S:</b> The skew shape (Green)</li>
 * <li><b>Z:</b> The skew shape (Red)</li>
 * </ul>
 * </p>
 */
public enum TetrominoType {
    I,
    O,
    T,
    J,
    L,
    S,
    Z
}